import turtle

# creating a window and storing it for later use
turtleWindow = turtle.Screen()

# Setting the title of the window
turtleWindow.title('turtle brah')

# Create turtle names Hendrix is the shape of a turtle
hendrix = turtle.Turtle(shape='turtle')


hendrix.pencolor('red')
hendrix.forward(100)
hendrix.pencolor('orange')
hendrix.left(90)
hendrix.forward(100)
hendrix.pencolor('pink')
hendrix.left(90)
hendrix.forward(100)
hendrix.pencolor('green')
hendrix.left(90)
hendrix.forward(100)
hendrix.left(90)









