import turtle

# creating a window and storing it for later use
turtleWindow = turtle.Screen()

# Setting the title of the window
turtleWindow.title('turtle brah')

# Create turtle names Hendrix is the shape of a turtle
hendrix = turtle.Turtle(shape='turtle')

for i in range(4):
    hendrix.forward(100)
    hendrix.left(90)






